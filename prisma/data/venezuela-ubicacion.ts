export type MunicipioCatalogo = { codigo: string; nombre: string };
export type EstadoCatalogo = {
  codigo: string;
  nombre: string;
  municipios: MunicipioCatalogo[];
};

// Catálogo con los `codigo` de municipio ya derivados (`<estado>-<nn>`). Es la
// estructura que consume el seeder.
export const CATALOGO_UBICACION: EstadoCatalogo[] = ESTADOS_VENEZUELA.map(
  (estado) => ({
    codigo: estado.codigo,
    nombre: estado.nombre,
    municipios: estado.municipios.map((nombre, indice) => ({
      codigo: `${estado.codigo}-${String(indice + 1).padStart(2, "0")}`,
      nombre,
    })),
  }),
);

export const TOTAL_ESTADOS = CATALOGO_UBICACION.length;
export const TOTAL_MUNICIPIOS = CATALOGO_UBICACION.reduce(
  (suma, estado) => suma + estado.municipios.length,
  0,
);
